package Week1;

public class ex7_Multiplication {
    public static void main(String[] args) {
        int a = 45, b = 11, c = 435, d = 567;
        System.out.println("The product of a and b: " + (a * b));
        System.out.println("The product of c and d: " + (c * d));
        //the highest number (int) that our program is able to compute is the 32-bit limit, or 2,147,483,647 (2^31 - 1);
    }
}
